def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b

x=int(input('Enter the first Number: '))
y=int(input("Enter the second Number: "))
print("Adddition: ",add(x,y))
print("Subtraction ",sub(x,y))
print("Multiplication: ",mul(x,y))
print("Division: ",div(x,y))